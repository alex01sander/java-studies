package praticando.exercicio;

public class Treino {
    private String nome;
    private int duracaoMinutos;
    private int caloriasQueimadas;
    private int totalParticipacoes;

    private int classificacao;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getDuracaoMinutos() {
        return duracaoMinutos;
    }

    public void setDuracaoMinutos(int duracaoMinutos) {
        this.duracaoMinutos = duracaoMinutos;
    }

    public int getCaloriasQueimadas() {
        return caloriasQueimadas;
    }

    public void setCaloriasQueimadas(int caloriasQueimadas) {
        this.caloriasQueimadas = caloriasQueimadas;
    }

    public int getTotalParticipacoes() {
        return totalParticipacoes;
    }

    public void setTotalParticipacoes(int totalParticipacoes) {
        this.totalParticipacoes = totalParticipacoes;
    }

    public int getClassificacao() {
        return classificacao;
    }




}
